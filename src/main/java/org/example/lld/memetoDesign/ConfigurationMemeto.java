package org.example.lld.memetoDesign;

public class ConfigurationMemeto {
    int height;
    int width;

    public ConfigurationMemeto(int height, int width){
        this.height=height;
        this.width=width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth(){
        return width;
    }
}
